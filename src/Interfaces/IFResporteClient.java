    public void llenar_tabla(){
        DefaultTableModel mtProd = (DefaultTableModel) this.jTableReporte.getModel();
        try {
            Llamado_BD llam = new Llamado_BD();
            ArrayList<Cliente> ltsClient = llam.llenar_Cliente();
            for(Cliente objC: ltsClient){
                int num = llam.obtenerCompras(objC);
                mtProd.addRow(new Object[]{num, objC.getApell_cliet() + " " + objC.getNomb_cliet(), objC.getDni_cliet()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR!!!\n" + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }
    public void tamanioTabla() {
        TableColumnModel columnModel = this.jTableReporte.getColumnModel();

        columnModel.getColumn(0).setPreferredWidth(100);
        columnModel.getColumn(1).setPreferredWidth(250);
        columnModel.getColumn(2).setPreferredWidth(100);
    }
