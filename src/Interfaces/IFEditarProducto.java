    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        if (this.validar() == 0) {
            JOptionPane.showMessageDialog(this, "Porfavor!!!\nComplete todos los campos", "Validar Llenado", JOptionPane.WARNING_MESSAGE);
        } else {
            this.validar_Marca_Provee();
            Producto_Proveedor objPPr = new Producto_Proveedor(
                    this.objP,
                    new Producto(this.jTextFieldCodigo.getText(),
                            this.jTextFieldNombre.getText(), Integer.parseInt(this.jTextFieldStock.getText()),
                            Float.parseFloat(this.jSpinnerPrecio.getValue().toString()),
                            this.objM));
            int id = this.objPP.getCod_produx();
            int id_produc = this.objPP.getId_pord().getCod_prod();
            objPPr.setCod_produx(id);
            objPPr.getId_pord().setCod_prod(id_produc);
            try {
                EditarBD edit = new EditarBD();
                edit.editar_Produc_Provee(objPPr);
                Escritorio.cargar_datos_Producto();
                JOptionPane.showMessageDialog(null, "Inserción exitosa!!!\n", "Inserción", JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
                this.activar_controles(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "OCURRIO UN ERROR: \n" + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

    private void jbtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtBuscarActionPerformed
        boolean existe = false;
        String buscar = this.jTextFieldBuscar.getText().trim();
        if (buscar.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese el código a buscar!!", "Buscar Producto", JOptionPane.WARNING_MESSAGE);
        } else {
            for (int i = 0; i < Escritorio.MTPP.getRowCount(); i++) {
                if (Escritorio.MTPP.getValueAt(i, 0).equals(buscar)) {
                    existe = true;
                    this.jTableProducProvee.changeSelection(i, 1, false, false);
                    this.jTextFieldBuscar.setText("");
                }
            }
            if (existe == false) {
                JOptionPane.showMessageDialog(this, "Código ingresado no existente.", "Buscar Producto", JOptionPane.INFORMATION_MESSAGE);
                this.jTextFieldBuscar.setText("");
            }
        }
    }//GEN-LAST:event_jbtBuscarActionPerformed

    private void jbtEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEditarActionPerformed
        int fila = this.jTableProducProvee.getSelectedRow();
        if (fila != -1) {
            this.objPP = Escritorio.MTPP.getProdPorevee(fila);
            this.cargarFormulario(objPP);
            this.activar_controles(true);
            Escritorio.cargar_datos_Producto();
        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado un elemento de la tabla", "Editar Cliente", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jbtEditarActionPerformed

