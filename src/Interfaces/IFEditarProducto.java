    private void cargar_combos() {
        this.jComboBoxMarca.removeAllItems();
        this.jComboBoxProvee.removeAllItems();
        //Cargar combo Marca
        Marca objM = new Marca();
        objM.setNomb_mar("-Seleccione-");
        this.jComboBoxMarca.addItem(objM);
        try {
            objM.mostrar_Marca(this.jComboBoxMarca);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR!!!\n" + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
        //Cargar combo Proveedor
        Proveedor objP = new Proveedor();
        objP.setNomb_provee("-Seleccione-");
        this.jComboBoxProvee.addItem(objP);
        try {
            objP.mostrar_Proveedor(this.jComboBoxProvee);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR!!!\n" + e.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void validar_llenado(java.awt.event.KeyEvent evt) {
        char validar = evt.getKeyChar();
        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();
        }
    }

    private int validar() {
        int lleno = 0;
        if (this.jTextFieldNombre.getText().trim().equals("")
                || this.jTextFieldStock.getText().trim().equals("")
                || this.jTextFieldCodigo.getText().trim().equals("")
                || this.jSpinnerPrecio.getValue().toString().equals("0")
                || this.jComboBoxMarca.getSelectedIndex() == 0
                || this.jComboBoxProvee.getSelectedIndex() == 0) {
            return lleno;
        } else {
            lleno = 1;
        }
        return lleno;
    }

    private void limpiar() {
        this.jTextFieldNombre.setText("");
        this.jTextFieldStock.setText("");
        this.jTextFieldCodigo.setText("");
        this.jSpinnerPrecio.setValue(0);
        this.jComboBoxMarca.setSelectedIndex(0);
        this.jComboBoxProvee.setSelectedIndex(0);

        this.objPP = null;
        this.objM = null;
        this.objP = null;
    }

    private void validar_tamañoTexto() {
        this.jTextFieldCodigo.setDocument(new Tamaño_TextField(this.jTextFieldCodigo, 20));
        this.jTextFieldNombre.setDocument(new Tamaño_TextField(this.jTextFieldNombre, 50));
        this.jTextFieldStock.setDocument(new Tamaño_TextField(this.jTextFieldStock, 3));
    }

    private void activar_controles(boolean estado) {
        this.jTextFieldCodigo.setEnabled(estado);
        this.jTextFieldNombre.setEnabled(estado);
        this.jTextFieldStock.setEnabled(estado);
        this.jSpinnerPrecio.setEnabled(estado);
        this.jComboBoxMarca.setEnabled(estado);
        this.jComboBoxProvee.setEnabled(estado);
        this.jbtActualizar.setEnabled(estado);
        this.jbtCancelar.setEnabled(estado);

        this.jTextFieldBuscar.setEnabled(!estado);
        this.jbtBuscar.setEnabled(!estado);
        this.jbtEditar.setEnabled(!estado);
        this.jTableProducProvee.setEnabled(!estado);
    }

    private void cargarFormulario(Producto_Proveedor objPP) {
        this.objM = objPP.getId_pord().getId_mar();
        this.objP = objPP.getId_prov();

        this.jTextFieldCodigo.setText(objPP.getId_pord().getCode());
        this.jTextFieldNombre.setText(objPP.getId_pord().getNomb_prod());
        this.jTextFieldStock.setText(String.valueOf(objPP.getId_pord().getStock_prod()));
        this.jSpinnerPrecio.setValue(objPP.getId_pord().getPrecio_prod());
        this.jComboBoxMarca.getModel().setSelectedItem(this.objM);
        this.jComboBoxProvee.getModel().setSelectedItem(this.objP);
    }

    private void definir_tamaño_columna() {
        TableColumnModel columnModel = this.jTableProducProvee.getColumnModel();

        columnModel.getColumn(0).setPreferredWidth(150);
        columnModel.getColumn(1).setPreferredWidth(400);
        columnModel.getColumn(2).setPreferredWidth(100);
        columnModel.getColumn(3).setPreferredWidth(90);
        columnModel.getColumn(4).setPreferredWidth(300);
    }

    private void validar_Marca_Provee() {
        if (this.jComboBoxProvee.getItemAt(this.jComboBoxProvee.getSelectedIndex()) != null) {
            this.objP = this.jComboBoxProvee.getItemAt(this.jComboBoxProvee.getSelectedIndex());
        }
        if (this.jComboBoxMarca.getItemAt(this.jComboBoxMarca.getSelectedIndex()) != null) {
            this.objM = this.jComboBoxMarca.getItemAt(this.jComboBoxMarca.getSelectedIndex());
        }
    }
