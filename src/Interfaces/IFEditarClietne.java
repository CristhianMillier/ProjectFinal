    private void jbtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtBuscarActionPerformed
        boolean existe = false;
        String buscar = this.jTextFieldBuscar.getText().trim();
        if (buscar.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese DNI del cliente a buscar!!", "Buscar Cliente", JOptionPane.WARNING_MESSAGE);
        } else {
            for (int i = 0; i < Escritorio.MTC.getRowCount(); i++) {
                if (Escritorio.MTC.getValueAt(i, 3).equals(buscar)) {
                    existe = true;
                    this.jTableCliente.changeSelection(i, 1, false, false);
                    this.jTextFieldBuscar.setText("");
                }
            }
            if (existe == false) {
                JOptionPane.showMessageDialog(this, "El DNI ingresado no existe.", "Buscar Cliente", JOptionPane.INFORMATION_MESSAGE);
                this.jTextFieldBuscar.setText("");
            }
        }
    }//GEN-LAST:event_jbtBuscarActionPerformed

    private void jbtEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEditarActionPerformed
        int fila = this.jTableCliente.getSelectedRow();
        if (fila != -1) {
            this.objC = Escritorio.MTC.getCliente(fila);
            this.cargarFormulario(objC);
            this.activar_controles(true);
            try {
                Escritorio.cargar_datos_Cliente();
            } catch (Exception ex) {
                Logger.getLogger(IFEditarClietne.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado un elemento de la tabla", "Editar Cliente", JOptionPane.WARNING_MESSAGE);
        }        
    }//GEN-LAST:event_jbtEditarActionPerformed

    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        if (this.validar() == 0) {
            JOptionPane.showMessageDialog(this, "Porfavor!!!\nComplete todos los campos", "Validar Llenado", JOptionPane.WARNING_MESSAGE);
        } else {
            this.definirSexo_Telef();
            Cliente objCli = new Cliente(this.jTextFieldNombre.getText(),
                    this.jTextFieldApellido.getText(),
                    String.valueOf(this.telef),
                    this.jTextFieldDni.getText(),
                    this.sexo,
                    this.jTextFieldDirecc.getText());
            int id = this.objC.getCod_clie();
            objCli.setCod_clie(id);
            try {
                EditarBD edit = new EditarBD();
                edit.editar_Cliente(objCli);
                Escritorio.cargar_datos_Cliente();
                JOptionPane.showMessageDialog(null, "Edicción exitosa!!!\n", "Editar Cliente", JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
                this.activar_controles(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "OCURRIO UN ERROR: \n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

