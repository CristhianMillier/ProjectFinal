    private void cargarFormulario(Cliente objC) {
        String sexo = "";
        if (objC.getSexo_cliet().equals("F")) {
            sexo = "Femenino";
        }else{
            sexo = "Masculino";
        }
        String num = String.valueOf(objC.getTelef_cliet());
        String telef1 = num.substring(0,3);
        String telef2 = num.substring(3,6);
        String telef3 = num.substring(6,9);
        this.jTextFieldNombre.setText(objC.getNomb_cliet());
        this.jTextFieldApellido.setText(objC.getApell_cliet());
        this.jTextFieldDni.setText(objC.getDni_cliet());
        this.jTextFieldTelef1.setText(telef1);
        this.jTextFieldTelef2.setText(telef2);
        this.jTextFieldTelef3.setText(telef3);
        this.jTextFieldDirecc.setText(objC.getDirec_cliet());
        this.jComboBoxSexo.setSelectedItem(sexo);
    }
