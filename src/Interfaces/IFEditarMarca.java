    private void jbtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtBuscarActionPerformed
        boolean existe = false;
        String buscar = this.jTextFieldBuscar.getText().trim();
        if (buscar.equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese la Marca a buscar!!", "Buscar marca", JOptionPane.WARNING_MESSAGE);
        } else {
            for (int i = 0; i < Escritorio.MTM.getRowCount(); i++) {
                if (Escritorio.MTM.getValueAt(i, 0).equals(buscar)) {
                    existe = true;
                    this.jTableMarca.changeSelection(i, 1, false, false);
                    this.jTextFieldBuscar.setText("");
                }
            }
            if (existe == false) {
                JOptionPane.showMessageDialog(this, "La Marca ingresada no existe.", "Buscar Marca", JOptionPane.INFORMATION_MESSAGE);
                this.jTextFieldBuscar.setText("");
            }
        }
    }//GEN-LAST:event_jbtBuscarActionPerformed

    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        if (this.jTextFieldNombre.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Porfavor!!!\nComplete todos los campos", "Validar Llenado", JOptionPane.WARNING_MESSAGE);
        } else {
            Marca objMa = new Marca(this.jTextFieldNombre.getText());
            int id = this.objM.getCod_mar();
            objMa.setCod_mar(id);
            try {
                EditarBD edit = new EditarBD();
                edit.editar_Marca(objMa);
                Escritorio.cargar_datos_Marca();
                JOptionPane.showMessageDialog(null, "Edicción exitosa!!!\n", "Editar Marca", JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
                this.activar_controles(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "OCURRIO UN ERROR: \n" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

