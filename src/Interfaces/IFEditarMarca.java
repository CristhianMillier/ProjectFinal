    private void limpiar() {
        this.jTextFieldNombre.setText("");

        this.objM = null;
    }

    private void activar_controles(boolean estado) {
        this.jTextFieldNombre.setEnabled(estado);
        this.jbtActualizar.setEnabled(estado);
        this.jbtCancelar.setEnabled(estado);

        this.jTextFieldBuscar.setEnabled(!estado);
        this.jbtBuscar.setEnabled(!estado);
        this.jbtEditar.setEnabled(!estado);
        this.jTableMarca.setEnabled(!estado);
    }

    private void cargarFormulario(Marca objM) {
        this.jTextFieldNombre.setText(objM.getNomb_mar());
    }

    private void validar_tamañoTexto() {
        this.jTextFieldNombre.setDocument(new Tamaño_TextField(this.jTextFieldNombre, 30));
        this.jTextFieldBuscar.setDocument(new Tamaño_TextField(this.jTextFieldBuscar, 30));
    }
