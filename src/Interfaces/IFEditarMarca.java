    private void jbtEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEditarActionPerformed
        int fila = this.jTableMarca.getSelectedRow();
        if (fila != -1) {
            this.objM = Escritorio.MTM.getMarca(fila);
            this.cargarFormulario(objM);
            this.activar_controles(true);
            try {
                Escritorio.cargar_datos_Marca();
            } catch (Exception ex) {
                Logger.getLogger(IFEditarMarca.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado un elemento de la tabla", "Editar Marca", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jbtEditarActionPerformed

